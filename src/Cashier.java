    private void jButton_buyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buyActionPerformed
        try {
            
            BufferedReader rdfile = new BufferedReader(new FileReader("items.txt"));
            
            String[] itemline = new String[1000];
            String str;
            double price, total;
            int qty = 0, prvQty = 0, newQty = 0;
            boolean found = false, edited = false;
            int id = (Integer.parseInt(jTextField_id.getText()))-101;

            for (int x = 0; (str = rdfile.readLine()) != null; x++) {
                itemline[x] = str;
            }
            rdfile.close();

            int r = Row.getRow();
            for (int i = 0; itemline[i] != null; i++) { 
                if (id == i) { // check if Product ID is equal to the row number
                    found = true;
                    String[] temp = itemline[i].trim().split("\t");
                    qty = Integer.parseInt(jTextField_qty.getText());
                    prvQty = Integer.parseInt(temp[1]);
                    if ((qty > prvQty) && (prvQty != 0)) {
                        JOptionPane.showMessageDialog(null, "Item Shortage!", "Oops!", JOptionPane.WARNING_MESSAGE);
                    }
                    if (prvQty == 0) {
                        JOptionPane.showMessageDialog(null, "Out of Stock!", "Oops!", JOptionPane.WARNING_MESSAGE);
                    }
                    if ((qty <= prvQty) && (prvQty != 0)) { // condition to be satified
                       
                        newQty = prvQty - qty;
                        itemline[i] = temp[0] + "\t" + newQty + "\t" + temp[2]; // replace the quantity of the product in the file by newQty
                        
                        model = (DefaultTableModel)jTable1.getModel();
                        model.setValueAt(newQty, id, 2); // 
                        
                        price = Double.parseDouble(temp[2]);
                        total = qty * price;
                        String itemName = temp[0];  
                        model = (DefaultTableModel)jTable2.getModel();
                        model.addRow(new Object [] {                            
                            itemName,
                            qty,
                            price,
                            total                            
                        });
                        
                        int row = jTable2.getRowCount();
                        int numOfItems = 0;
                        double totalPrice = 0;
                        for (int a= 0; a<row; a++){
                             numOfItems += (Integer.parseInt(jTable2.getValueAt(a, 1).toString()));
                             totalPrice += (Double.parseDouble(jTable2.getValueAt(a,3).toString()));
                        }
                        jTextField_numberOfItems.setText(Integer.toString(numOfItems));
                        jTextField_totalprice.setText(Double.toString(totalPrice));
                        
                        r++;
                        edited = true;
                    }                   
                }              
            }
            
            if (!found) {
                JOptionPane.showMessageDialog(null, jTextField_id.getText() + ": Item Not Available!", "Oops!", JOptionPane.ERROR_MESSAGE);
            }
            if (edited) {
                PrintWriter wrfile = new PrintWriter(new FileWriter("items.txt"));
                for (int i = 0; itemline[i] != null; i++) {
                    wrfile.println(itemline[i]);
                }               
                wrfile.close();
                Row.setRow();
                jButton_pay.setEnabled(true);
                jTextField_id.setText("");
                jTextField_qty.setText("");
            }
                
            jTextField_id.setText("");
            jTextField_qty.setText("");

        } catch (IOException e) {
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input!", "", JOptionPane.ERROR_MESSAGE);
            jTextField_id.setText("");
            jTextField_qty.setText("");
        }                                     
    }//GEN-LAST:event_jButton_buyActionPerformed

    private void jButton_payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_payActionPerformed
        try {               
            double totalPrice, cash;
            do {  
                cash = Double.parseDouble(jTextField_payment.getText());
                totalPrice = (Double.parseDouble(jTextField_totalprice.getText()));                
                jTextField_payment.setText("");               
                if (cash < totalPrice) {
                    JOptionPane.showMessageDialog(null, "Insufficient Cash!", null, JOptionPane.WARNING_MESSAGE);
                }
            } while (cash < totalPrice);
            jTextField_change.setText("" + (cash - totalPrice));
            jTextField_payment.setText("" + cash);
            jButton_buy.setEnabled(false);
        } catch (NumberFormatException e) {
        }
    }//GEN-LAST:event_jButton_payActionPerformed

