    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        model = (DefaultTableModel)jTable2.getModel();
        
        int selectedRow = jTable2.getSelectedRow();
        model.removeRow(selectedRow);
        
        int row = jTable2.getRowCount();
        int numOfItems = 0;
        double totalPrice = 0;
                        
        for (int a= 0; a<row; a++){
            numOfItems += (Integer.parseInt(jTable2.getValueAt(a, 1).toString()));
            totalPrice += (Double.parseDouble(jTable2.getValueAt(a,3).toString()));
        }
        jTextField_numberOfItems.setText(Integer.toString(numOfItems));
        jTextField_totalprice.setText(Double.toString(totalPrice));
    }//GEN-LAST:event_jButton_deleteActionPerformed

